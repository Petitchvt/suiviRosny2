// Fonction pour trouver l'acheteur d'un laboratoire (matching souple)
export function getAcheteurDuLabo(nomLabo) {
  const nomNorm = nomLabo.trim().toUpperCase();
  for (const acheteur of ACHETEURS) {
    for (const labo of acheteur.laboratoires) {
      // Matching souple : le nom en base peut être une version simplifiée
      const laboNorm = labo.trim().toUpperCase();
      // Match exact ou si l'un contient l'autre (pour gérer les suffixes 2026 //)
      const laboBase = laboNorm.replace(/\s*\d{4}\s*\/\/.*$/, '').replace(/\s*\*+$/, '').trim();
      const nomBase = nomNorm.replace(/\s*\d{4}\s*\/\/.*$/, '').replace(/\s*\*+$/, '').trim();
      if (laboNorm === nomNorm || laboBase === nomBase || nomNorm.startsWith(laboBase) || laboBase.startsWith(nomBase)) {
        return acheteur;
      }
    }
  }
  return null;
}